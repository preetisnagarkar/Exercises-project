class Student:

	def  __information__(self, name, grade, age):
	    self.name = name
	    self.grade = grade
	    self.age = age




kitty = Student('kitty', 85, 15)
deniel = Student('deniel', 70, 25)

print("Name1: ", kitty.name)
print("Name2: ", deniel.name)

print("Grade1: ", kitty.grade)
print("Grade2: ", deniel.grade)

print("Age1: ", kitty.age)
print("Age2: ", deniel.age)

